<template>
    <v-app :theme="theme">
        <v-main>
            <Navbar />
            <ToDo />
        </v-main>
        <v-btn :prepend-icon="theme === 'light' ? 'mdi-weather-sunny' : 'mdi-weather-night'" text="Toggle Theme" slim @click="onClick"></v-btn>
    </v-app>
</template>

<script setup>
    import { ref } from 'vue'

    const theme = ref('light')

    function onClick() {
        theme.value = theme.value === 'light' ? 'dark' : 'light'
    }
</script>

